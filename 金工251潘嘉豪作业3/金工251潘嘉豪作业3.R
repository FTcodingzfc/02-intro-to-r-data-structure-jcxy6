1、R语言最最基本的数据结构是向量，可以用c()函数来构建向量。数据类
型有数值型、字符型、逻辑型、整数型、复数型。用class()来查看数据结
构的类型。可以用[]来按照序号提取元素，书写在创建的向量后面，如ab<-c(1,23,12,221),
ab[2],运行后会显示数值23
c()#创建向量
class()#查看向量类型
ab<-c(1,2,3,4)#给ab赋值向量
ab[2]#按照序号提取ab中序号为2的值
2、在R语言中列表可以包含任何其他类型的数据结构，用list()来创建。
如zzz<-list(shu=c(1,2,3),zi=c("sadhu"))#进行zzz列表的构建，包含数值型和字符型
提取元素的方法有2种，可以使用[[]]#返回元素本身
也可以使用$来按照名称提取，如zzz$shu#提取zzz列表中子列表shu的元素
3、如果要进行常规的数据统计分析，需要用到数据框。数据框是特殊的列表，每列的元素是向量，是前两种数据结构的结合。
k<-data.frame(shu=c(11,22,),zi=c("mingchao","qidong"))#创建数据框k 
4、向量：最基础单元，所以元素必须同类型。列表：向量的“超集”，可包含任意类型的向量，元素可异。
数据框：特殊的列表，每一列是向量，每列的元素可异。
代码检查：
1、class(k)#检查数据框k的类型，输出结果："data.frame"
   is.list(k)#检查数据框k是否是列表，输出结果：TRUE
   所以数据框是特殊的列表
2、class(k$shu)#检查数据框的列是向量，输出结果："numeric"是向量类型
所以数据框的列是向量
